    private void loginBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBTNActionPerformed
         // Retrieve values from input fields
    String userID = userIDTextField.getText().trim();
    String password = new String(passwordPasswordField.getPassword());

    // Validate fields
    if (!ValidationUtils.validateLoginFields(userID, password)) {
        return;  // Exit the method if validation fails
    }

    // Perform login authentication
    User authenticatedUser = authenticateUser(userID, password);

    // Check if authentication was successful
    if (authenticatedUser != null) {
        // Redirect to the appropriate dashboard based on the user's role
        if (authenticatedUser.getRole().equals("Admin")) {
    AdminDashboard admin = new AdminDashboard();

    // Make the AdminDashboard form visible
    admin.setVisible(true);

    // Close or hide the current login form
    this.setVisible(false); // Hide the current form
    // OR
    this.dispose(); // Close the current form
} else if (authenticatedUser.getRole().equals("User")) {
    UserDashBoard userDashboard = new UserDashBoard();

    // Assuming updateUserDetails sets user details in the dashboard
    userDashboard.updateUserDetails(
        authenticatedUser.getUserID(),
        authenticatedUser.getFullNames(),
        authenticatedUser.getPhoneNumber(),
        authenticatedUser.getPosition()
    );

    // Make the UserDashboard form visible
    userDashboard.setVisible(true);

    // Close or hide the current login form
    this.setVisible(false); // Hide the current form
    // OR
    this.dispose(); // Close the current form
}

    } else {
        // Display error message for unsuccessful login
        JOptionPane.showMessageDialog(this, "Invalid credentials. Please try again.", "Error", JOptionPane.ERROR_MESSAGE);
    }
        
        
    }//GEN-LAST:event_loginBTNActionPerformed

