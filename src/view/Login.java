    private User authenticateUser(String userID, String password) {
        UserDao userDao = new UserDao("jdbc:mysql://localhost:3306/construction_management_system_db", "root", "");

        // Call the getUserByCredentials method from UserDao
        return userDao.getUserByCredentials(userID, password);
    }

